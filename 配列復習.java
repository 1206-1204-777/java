public class 配列復習 {
    public static void main(String[] args) {
        //変数と配列の違い
        String test = "Hello ";
        String test2 = "wold";
        System.out.println(test+test2);     //変数を使用した出力

        System.out.println();

        String [] word = {"java","mine"};   //配列の作成
        System.out.println(test+word[0]);   //変数と配列を組み合わせて出力    

    }
}